CREATE TABLE "STOCK_000001"(
            UUID VARCHAR2(80) PRIMARY KEY,
            DATETIME DATE NOT NULL,
            CODE VARCHAR2(20),
            C_NAME VARCHAR2(80),
            INDUSTRY VARCHAR2(80),
            CLASSIFY VARCHAR(80),
            OPEN NUMBER(20, 2),
            CLOSE NUMBER(20, 2),
            HIGH NUMBER(20, 2),
            LOW NUMBER(20, 2),
            VOLUME NUMBER(20, 1),
            AMOUNT NUMBER(20, 1),
            Y_CLOSE NUMBER(20, 2),
            P_CHANGE NUMBER(20, 2),
            P_CHANGE_RATE NUMBER(20, 6)
        );

